let a = 10
let b = 5


//adição
let c = a + b
console.log(c)

//subtração
let d = a - b
console.log(d)

//multiplicação
let e = a * b
console.log(e)

//divisão
let f = a / b
console.log(f)

//incrementação
++a
console.log(a++)
